int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i <= nb / 2 && nb < 5000)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	while (i <= nb / 50)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
