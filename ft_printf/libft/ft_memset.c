#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	size_t	i;

	ptr = b;
	i = 0;
	while (len--)
	{
		*(ptr + i) = c;
		i++;
	}
	return (ptr);
}
